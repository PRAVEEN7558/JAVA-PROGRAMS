package Recursion;

public class AddtionNumber_recursion {

	public static void main(String[] args) {

		for (int a = 1, b = 5; a <= 5; a++, b--) {
			System.out.println("A is  " + a);
			System.out.println("           B is " + b);
		}

		
		
		int i = 1;
		for (; i <= 5;) {
			System.out.println(i);
			i++;
		}
		
		for (int a = 1, b = 5; a <= 5; a++, b++) {
			System.out.println("A is  " + a);
			System.out.println("           B is " + b);
	}
}}
