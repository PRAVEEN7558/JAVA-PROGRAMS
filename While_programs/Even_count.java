// Printing even number 1 to 10? 
package While_programs;
public class Even_count 
{
	  public static void main(String[] args) 
	  {
	        int even = 1;
	        while (even<=10) 
	        {
	        	if (even%2==0)
	        	{
	        		
	            System.out.println(even);
	        	}
	            even++;
	        }
	   }
}

